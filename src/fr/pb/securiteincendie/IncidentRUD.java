    private void jButtonModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModifierActionPerformed
        jLabelMessage.setText("");
        int liRow = jTableIncidents.getSelectedRow();
        if (liRow >= 0) {
            if (jTableIncidents.getValueAt(liRow, 0).toString().equals("") || jTableIncidents.getValueAt(liRow, 0).toString().equals("-")) {
                jTableIncidents.setValueAt("v", liRow, 0);
                jTableIncidents.setValueAt(jTextFieldDateHeure.getText(), liRow, 1);
                jTableIncidents.setValueAt(jComboBoxLieux.getSelectedItem().toString(), liRow, 2);
                jTableIncidents.setValueAt(jComboBoxCauses.getSelectedItem().toString(), liRow, 3);
                jTableIncidents.setValueAt(jComboBoxPersonnes.getSelectedItem().toString(), liRow, 4);
                jLabelMessage.setText("Modification pré-effectuée");
            }
        } else {
            jLabelMessage.setText("Aucune ligne sélectionnée");
        }

    }//GEN-LAST:event_jButtonModifierActionPerformed

    private void jButtonCommitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCommitActionPerformed
        jLabelMessage.setText("");

        try {

            String lsDateHeure;
            String lsLieuCause;

            // Récupération du nombre de lignes de la jtable
            int liRows = jTableIncidents.getRowCount();
            // Boucle sur les lignes dans la jtable
            for (int i = liRows - 1; i >= 0; i--) {
                //Si flag = -
                if (jTableIncidents.getValueAt(i, 0).toString().equals("-")) {
                    //Suppression
//                    System.out.println("N°1 - " + jTableIncidents.getValueAt(i, 1).toString());
//                    String lsIdIncident = jedis.get("IdIncident");
//                    String lsIncident = "Incident" + lsIdIncident;
//                    System.out.println("N°2 - " + lsIncident);
                    jedis.lrem("IncidentsListe", 1, jTableIncidents.getValueAt(i, 1).toString());
                    jedis.del(jTableIncidents.getValueAt(i, 1).toString());
                    idtm.removeRow(i);
                }

                //Si flag =  v 
                if (jTableIncidents.getValueAt(i, 0).toString().equals("v")) {
                    Map<String, String> map = new HashMap();

                    lsDateHeure = jTableIncidents.getValueAt(i, 2).toString();
                    lsLieuCause = jTableIncidents.getValueAt(i, 3).toString() + ":" + jTableIncidents.getValueAt(i, 4).toString();

                    map.put("id", jTableIncidents.getValueAt(i, 1).toString());
                    map.put("date", lsDateHeure);
                    map.put("lieu", jTableIncidents.getValueAt(i, 3).toString());
                    map.put("cause", jTableIncidents.getValueAt(i, 4).toString());
                    map.put("personne", jTableIncidents.getValueAt(i, 5).toString());

                    //Ajout des valeurs
                    jedis.hmset(jTableIncidents.getValueAt(i, 1).toString(), map);
                    jTableIncidents.setValueAt("", i, 0);
                }

                

            }
        } catch (Exception e) {
            jLabelMessage.setText(e.getMessage());
        }
    }//GEN-LAST:event_jButtonCommitActionPerformed

